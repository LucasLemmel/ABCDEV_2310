package exercicetableaux2;

public class App {

	public static void main(String[] args) {
		
		
		
		
		
		String[] tableau = new String[6];
		
		
		
		tableau[0] = "A";
		tableau[1] = "E";
		tableau[2] = "I";
		tableau[3] = "O";
		tableau[4] = "U";
		tableau[5] = "Y";
	
	
		for(int j = 0;j <= tableau.length-1;j++ )
		{
			System.out.print(tableau[j]+" ");
		}
		
		System.out.println("\n");

	}

}
